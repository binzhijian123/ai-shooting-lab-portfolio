const stage = document.getElementById("posterStage");
const posterFrame = document.getElementById("posterFrame");
const currentBackdrop = document.getElementById("monoBackdrop");
const nextBackdrop = document.getElementById("monoBackdropNext");
const currentLayer = document.getElementById("athleteLayer");
const nextLayer = document.getElementById("athleteLayerNext");
const currentImage = document.getElementById("athleteImage");
const nextImage = document.getElementById("athleteImageNext");
const posterDate = document.getElementById("posterDate");
const title = document.getElementById("slideTitle");
const kicker = document.getElementById("slideKicker");
const togglePlay = document.getElementById("togglePlay");
const transitionAthlete = document.getElementById("transitionAthlete");

let activeIndex = getStoredPosterSlideIndex();
let playing = true;
let timer = null;
let locked = false;

init();

function init() {
  if (window.location.hash && document.querySelector(window.location.hash)) {
    document.body.classList.remove("portal-mode");
    document.body.classList.add("workbench-mode");
    const route = routeFromHash(window.location.hash);
    document.body.dataset.role = route.role;
    document.body.dataset.tab = route.tab;
  }
  updatePosterDate();
  applySlide(slides[activeIndex], {
    layer: currentLayer,
    image: currentImage,
    backdrop: currentBackdrop
  });
  updateLabels(slides[activeIndex]);
  bindControls();
  window.setInterval(updatePosterDate, 60 * 1000);
  schedule();
}

function getStoredPosterSlideIndex() {
  const raw = Number.parseInt(sessionStorage.getItem("arcLabPosterSlideIndex") || "0", 10);
  if (!Number.isFinite(raw)) return 0;
  return Math.min(Math.max(raw, 0), slides.length - 1);
}

function updatePosterDate() {
  const formatted = new Intl.DateTimeFormat("en-US", {
    weekday: "short",
    month: "short",
    day: "numeric"
  }).format(new Date());
  posterDate.textContent = formatted.replace(",", "");
}

function bindControls() {
  document.getElementById("prevSlide").addEventListener("click", () => go(-1));
  document.getElementById("nextSlide").addEventListener("click", () => go(1));
  document.querySelectorAll(".poster-cta").forEach((link) => link.addEventListener("click", enterLab));
  togglePlay.addEventListener("click", () => {
    playing = !playing;
    togglePlay.textContent = playing ? "Pause" : "Play";
    togglePlay.setAttribute("aria-label", playing ? "Pause carousel" : "Play carousel");
    if (playing) schedule();
    else clearTimeout(timer);
  });
  window.addEventListener("keydown", (event) => {
    if (event.key === "ArrowLeft") go(-1);
    if (event.key === "ArrowRight") go(1);
    if (event.key === " ") {
      event.preventDefault();
      togglePlay.click();
    }
  });
}

function enterLab(event) {
  event.preventDefault();
  if (stage.classList.contains("is-docking-lab")) return;
  const targetUrl = new URL(event.currentTarget.href);
  const samePageTarget = targetUrl.origin === window.location.origin && targetUrl.pathname === window.location.pathname && targetUrl.hash;
  if (samePageTarget) {
    clearTimeout(timer);
    playing = false;
    togglePlay.textContent = "Play";
    togglePlay.setAttribute("aria-label", "Play carousel");
    transitionAthlete.src = slides[activeIndex].subjectImage || slides[activeIndex].image;
    syncTransitionPlayerSize();
    stage.classList.add("is-entering-lab");
    window.setTimeout(() => {
      document.body.classList.remove("portal-mode");
      document.body.classList.add("workbench-mode");
      const route = routeFromHash(targetUrl.hash);
      document.body.dataset.role = route.role;
      document.body.dataset.tab = route.tab;
      window.dispatchEvent(new CustomEvent("arc-lab-workbench-change", { detail: route }));
      document.querySelector(targetUrl.hash)?.scrollIntoView({ behavior: prefersReducedMotion() ? "auto" : "smooth", block: "start" });
      window.setTimeout(() => stage.classList.remove("is-entering-lab"), prefersReducedMotion() ? 80 : 520);
    }, prefersReducedMotion() ? 60 : 620);
    return;
  }
  targetUrl.searchParams.set("intro", "1");
  targetUrl.hash = "";
  const targetHref = targetUrl.toString();
  clearTimeout(timer);
  playing = false;
  togglePlay.textContent = "Play";
  togglePlay.setAttribute("aria-label", "Play carousel");
  sessionStorage.setItem("arcLabPosterSlideIndex", String(activeIndex));
  sessionStorage.setItem("arcLabIntroPending", "1");
  syncPosterDockingMetrics();
  currentImage.getBoundingClientRect();
  stage.classList.add("is-docking-lab");
  window.setTimeout(() => {
    window.location.href = targetHref;
  }, prefersReducedMotion() ? 60 : 620);
}

function syncPosterDockingMetrics() {
  const frameRect = posterFrame.getBoundingClientRect();
  const titleRect = document.querySelector(".poster-copy").getBoundingClientRect();
  const titleContentRect = getTitleContentRect();
  const playerRect = currentImage.getBoundingClientRect();
  const backdropRect = currentBackdrop.getBoundingClientRect();
  const target = getDockedBannerTargets();
  setInsetDockVars("frame", frameRect, target.frame);
  setContainedDockVars("title", titleRect, titleContentRect, target.title);
  setCenterDockVars("player", playerRect, target.player);
  setBoxDockVars("backdrop", backdropRect, target.frame);
}

function getDockedBannerTargets() {
  const viewportWidth = window.innerWidth || document.documentElement.clientWidth;
  const isMobile = viewportWidth <= 900;
  const workspacePad = isMobile ? 10 : 18;
  const mastheadTop = 72 + workspacePad;

  if (isMobile) {
    const titleLeft = workspacePad + 18;
    const titleTop = mastheadTop + 23;
    const titleWidth = viewportWidth - titleLeft * 2;
    const playerWidth = Math.min(viewportWidth * 0.78, 300);
    return {
      frame: {
        left: workspacePad,
        top: mastheadTop,
        width: viewportWidth - workspacePad * 2,
        height: 582
      },
      title: { left: titleLeft, top: titleTop, width: titleWidth, height: 205 },
      player: {
        left: (viewportWidth - playerWidth) / 2,
        top: mastheadTop + 235,
        width: playerWidth,
        height: 210
      }
    };
  }

  const titleLeft = workspacePad + 34;
  const titleTop = mastheadTop + 30;
  const titleWidth = Math.min(viewportWidth * 0.32, 560);
  const playerWidth = Math.min(viewportWidth * 0.28, 520);
  return {
    frame: {
      left: workspacePad,
      top: mastheadTop,
      width: viewportWidth - workspacePad * 2,
      height: 266
    },
    title: { left: titleLeft, top: titleTop, width: titleWidth, height: 190 },
    player: {
      left: (viewportWidth - playerWidth) / 2,
      top: mastheadTop + 16,
      width: playerWidth,
      height: 260
    }
  };
}

function getTitleContentRect() {
  const dateRect = posterDate.getBoundingClientRect();
  const headlineRect = document.querySelector(".poster-copy h1").getBoundingClientRect();
  return {
    left: Math.min(dateRect.left, headlineRect.left),
    top: Math.min(dateRect.top, headlineRect.top),
    right: Math.max(dateRect.right, headlineRect.right),
    bottom: Math.max(dateRect.bottom, headlineRect.bottom),
    get width() {
      return this.right - this.left;
    },
    get height() {
      return this.bottom - this.top;
    }
  };
}

function setContainedDockVars(name, parentRect, contentRect, targetRect) {
  const scale = Math.min(targetRect.width / contentRect.width, targetRect.height / contentRect.height);
  const x = targetRect.left - parentRect.left - (contentRect.left - parentRect.left) * scale;
  const y = targetRect.top - parentRect.top - (contentRect.top - parentRect.top) * scale;
  stage.style.setProperty(`--dock-${name}-x`, `${Math.round(x)}px`);
  stage.style.setProperty(`--dock-${name}-y`, `${Math.round(y)}px`);
  stage.style.setProperty(`--dock-${name}-scale`, String(Number(scale.toFixed(3))));
}

function setCenterDockVars(name, sourceRect, targetRect) {
  const sourceX = sourceRect.left + sourceRect.width / 2;
  const sourceY = sourceRect.top + sourceRect.height / 2;
  const targetX = targetRect.left + targetRect.width / 2;
  const targetY = targetRect.top + targetRect.height / 2;
  const scale = Math.min(targetRect.width / sourceRect.width, targetRect.height / sourceRect.height);
  stage.style.setProperty(`--dock-${name}-x`, `${Math.round(targetX - sourceX)}px`);
  stage.style.setProperty(`--dock-${name}-y`, `${Math.round(targetY - sourceY)}px`);
  stage.style.setProperty(`--dock-${name}-scale`, String(Number(scale.toFixed(3))));
}

function setBoxDockVars(name, sourceRect, targetRect) {
  const scaleX = targetRect.width / sourceRect.width;
  const scaleY = targetRect.height / sourceRect.height;
  stage.style.setProperty(`--dock-${name}-x`, `${Math.round(targetRect.left - sourceRect.left)}px`);
  stage.style.setProperty(`--dock-${name}-y`, `${Math.round(targetRect.top - sourceRect.top)}px`);
  stage.style.setProperty(`--dock-${name}-scale-x`, String(Number(scaleX.toFixed(4))));
  stage.style.setProperty(`--dock-${name}-scale-y`, String(Number(scaleY.toFixed(4))));
}

function setInsetDockVars(name, sourceRect, targetRect) {
  const top = Math.max(0, targetRect.top - sourceRect.top);
  const right = Math.max(0, sourceRect.right - targetRect.left - targetRect.width);
  const bottom = Math.max(0, sourceRect.bottom - targetRect.top - targetRect.height);
  const left = Math.max(0, targetRect.left - sourceRect.left);
  stage.style.setProperty(`--dock-${name}-top`, `${Math.round(top)}px`);
  stage.style.setProperty(`--dock-${name}-right`, `${Math.round(right)}px`);
  stage.style.setProperty(`--dock-${name}-bottom`, `${Math.round(bottom)}px`);
  stage.style.setProperty(`--dock-${name}-left`, `${Math.round(left)}px`);
}

function syncTransitionPlayerSize() {
  const currentRect = currentImage.getBoundingClientRect();
  const viewportWidth = window.innerWidth || document.documentElement.clientWidth;
  const viewportHeight = window.innerHeight || document.documentElement.clientHeight;
  const targetWidth = Math.min(viewportWidth * 0.48, 560);
  const targetWidthMobile = Math.min(viewportWidth * 0.72, 420);
  const startOffsetY = Math.max(-80, Math.min(80, currentRect.top + currentRect.height / 2 - viewportHeight / 2));
  stage.style.setProperty("--transition-player-start-width", `${Math.round(currentRect.width)}px`);
  stage.style.setProperty(
    "--transition-player-end-width",
    `${Math.round(viewportWidth <= 720 ? targetWidthMobile : targetWidth)}px`
  );
  stage.style.setProperty("--transition-player-start-y", `${Math.round(startOffsetY * 0.18)}px`);
}

function go(direction) {
  goTo((activeIndex + direction + slides.length) % slides.length);
}

function goTo(nextIndex) {
  if (locked || nextIndex === activeIndex) return;
  document.activeElement?.blur?.();
  window.scrollTo(0, 0);
  locked = true;
  clearTimeout(timer);

  const incoming = slides[nextIndex];
  applySlide(incoming, {
    layer: nextLayer,
    image: nextImage,
    backdrop: nextBackdrop
  });
  updateLabels(incoming);
  stage.classList.add("is-transitioning");

  window.setTimeout(() => {
    activeIndex = nextIndex;
    applySlide(incoming, {
      layer: currentLayer,
      image: currentImage,
      backdrop: currentBackdrop
    });
    stage.classList.remove("is-transitioning");
    locked = false;
    if (playing) schedule();
  }, 760);
}

function schedule() {
  clearTimeout(timer);
  if (!playing) return;
  timer = window.setTimeout(() => go(1), 3600);
}

function updateLabels(slide) {
  title.textContent = slide.title;
  kicker.textContent = slide.kicker;
}

function applySlide(slide, targets) {
  targets.image.src = slide.subjectImage || slide.image;
  targets.backdrop.style.setProperty("--image", `url("${slide.image}")`);
  targets.backdrop.style.setProperty("--bg-position", slide.bgPosition);
  for (const element of [targets.layer, targets.backdrop]) {
    element.style.setProperty("--subject-position", slide.subjectPosition);
    element.style.setProperty("--subject-width", slide.subjectWidth);
    element.style.setProperty("--subject-height", slide.subjectHeight);
    element.style.setProperty("--subject-width-mobile", slide.subjectWidthMobile);
    element.style.setProperty("--subject-height-mobile", slide.subjectHeightMobile);
    element.style.setProperty("--subject-zoom", slide.subjectZoom);
    element.style.setProperty("--subject-x", slide.subjectX);
    element.style.setProperty("--subject-y", slide.subjectY);
    element.style.setProperty("--mask-x", slide.maskX);
    element.style.setProperty("--mask-y", slide.maskY);
    element.style.setProperty("--focus-x", slide.focusX);
    element.style.setProperty("--focus-y", slide.focusY);
  }
}

function prefersReducedMotion() {
  return window.matchMedia("(prefers-reduced-motion: reduce)").matches;
}

function routeFromHash(hash) {
  if (hash === "#student") return { role: "student", tab: "knowledge" };
  if (hash === "#student-home") return { role: "student", tab: "home" };
  return { role: "coach", tab: "home" };
}
