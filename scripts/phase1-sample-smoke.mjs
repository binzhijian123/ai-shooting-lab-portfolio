import { spawn } from "node:child_process";
import { readFile } from "node:fs/promises";
import { createServer } from "node:net";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const nodeBin = process.execPath;

const manifest = JSON.parse(await readFile(path.join(root, "data", "sample_manifest.json"), "utf8"));
const sampleId = readArg("--sample-id") || "synthetic_ball";
const sample = manifest.samples?.find((item) => item.id === sampleId);
if (!sample) fail(`sample not found in data/sample_manifest.json: ${sampleId}`);
if (sample.authorization?.status !== "authorized") fail(`sample is not authorized: ${sampleId}`);
if (!sample.authorization?.scope?.includes("local_acceptance_test")) {
  fail(`sample is not authorized for local_acceptance_test: ${sampleId}`);
}

const port = Number(readArg("--port")) || await freePort();
const baseUrl = readArg("--base-url") || `http://localhost:${port}`;
const startedServer = !readArg("--base-url");
let serverProcess = null;
let uploadId = null;
let sessionId = null;
const cleanupErrors = [];

try {
  if (startedServer) {
    serverProcess = await startServer(port);
  } else {
    await waitForServer(baseUrl);
  }

  const upload = await uploadSample(baseUrl, sample);
  uploadId = upload.upload_id;
  assert(upload.upload_id, "upload_id is required");
  assert(Number(upload.bytes || 0) > 0, "upload bytes must be positive");

  const evidence = await postJson(`${baseUrl}/api/analyze-video`, {
    upload_id: upload.upload_id,
    file_name: upload.file_name,
    shot_type: sample.shot_type,
    camera_view: sample.camera_view,
    fps: sample.fps || 60,
    training_goal: "Phase 1 local smoke test"
  });
  assert(evidence.schema_version === "evidence_packet.v1", "evidence schema mismatch");
  assert(evidence.pipeline_status?.video_layer === "local_upload_ready", "video layer is not ready");
  assert(evidence.pipeline_status?.memory_layer === "sqlite_sessions", "memory layer is not sqlite_sessions");
  assert(evidence.confidence?.max_report_confidence === "low", "synthetic smoke should remain low confidence");
  assert(Array.isArray(evidence.missing_evidence), "missing_evidence must be an array");

  const coach = await postJson(`${baseUrl}/api/coach-report`, evidence);
  assert(["local_mock", "evidence_insufficient_fallback", "request_validation_fallback"].includes(coach.mode), `unexpected coach mode: ${coach.mode}`);
  assert(coach.report?.primary_diagnosis, "coach report primary_diagnosis missing");
  assert(coach.player_report?.schema_version === "player_report.v1", "player report contract missing");
  assert(coach.lab_report?.schema_version === "lab_report.v1", "lab report contract missing");

  sessionId = `phase1_smoke_${Date.now()}`;
  const saved = await postJson(`${baseUrl}/api/sessions`, {
    session_id: sessionId,
    title: `Phase 1 smoke: ${sample.id}`,
    evidence,
    report: coach.report,
    memory_status: "short_term_review",
    feedback: {
      shot_result: "synthetic",
      coach_helpfulness: "smoke_test",
      note: "Generated by scripts/phase1-sample-smoke.mjs"
    }
  });
  assert(saved.ok === true, "session save failed");
  assert(saved.memory_status === "short_term_review", "smoke session must stay short_term_review");
  assert(saved.long_term_written === false, "smoke session must not write long-term memory");

  const memory = await fetchJson(`${baseUrl}/api/memory-summary`);
  assert(memory.confidence_policy?.trend_source === "long_term_only", "memory trend source must be long_term_only");

  const summary = {
    ok: true,
    sample_id: sample.id,
    upload: {
      upload_id: upload.upload_id,
      bytes: upload.bytes,
      metadata_status: upload.metadata?.status || null
    },
    evidence: {
      schema_version: evidence.schema_version,
      analysis_mode: evidence.session?.analysis_mode,
      max_report_confidence: evidence.confidence?.max_report_confidence,
      missing_evidence_count: evidence.missing_evidence.length,
      object_detection_layer: evidence.pipeline_status?.object_detection_layer,
      precision_layer: evidence.pipeline_status?.precision_layer
    },
    coach: {
      mode: coach.mode,
      validation_errors: coach.validation_errors || []
    },
    saved: {
      ok: saved.ok,
      memory_status: saved.memory_status,
      long_term_written: saved.long_term_written
    },
    memory: {
      trend_source: memory.confidence_policy?.trend_source,
      review_sessions_excluded: memory.confidence_policy?.review_sessions_excluded
    }
  };
  console.log(JSON.stringify(summary, null, 2));
} finally {
  if (sessionId) {
    await fetch(`${baseUrl}/api/sessions/${encodeURIComponent(sessionId)}`, { method: "DELETE" })
      .then((response) => response.ok || cleanupErrors.push(`session cleanup status ${response.status}`))
      .catch((error) => cleanupErrors.push(`session cleanup failed: ${error.message}`));
  }
  if (uploadId) {
    await fetch(`${baseUrl}/api/uploads/${encodeURIComponent(uploadId)}`, { method: "DELETE" })
      .then((response) => response.ok || cleanupErrors.push(`upload cleanup status ${response.status}`))
      .catch((error) => cleanupErrors.push(`upload cleanup failed: ${error.message}`));
  }
  if (serverProcess) {
    serverProcess.kill("SIGINT");
    await new Promise((resolve) => serverProcess.once("exit", resolve));
  }
  if (cleanupErrors.length) {
    console.error(`cleanup warnings: ${cleanupErrors.join("; ")}`);
    process.exitCode = process.exitCode || 1;
  }
}

async function startServer(port) {
  const child = spawn(nodeBin, ["server/index.mjs"], {
    cwd: root,
    env: {
      ...process.env,
      PORT: String(port),
      DEEPSEEK_API_KEY: "",
      YOLO_COMMAND: "",
      RTMPOSE_COMMAND: ""
    },
    stdio: ["ignore", "pipe", "pipe"]
  });
  let stderr = "";
  child.stderr.on("data", (chunk) => {
    stderr += chunk.toString();
  });
  child.on("exit", (code) => {
    if (code && code !== 130 && code !== 1) {
      console.error(`server exited with code ${code}: ${stderr.slice(-1000)}`);
    }
  });
  try {
    await waitForServer(`http://localhost:${port}`);
    return child;
  } catch (error) {
    child.kill("SIGINT");
    await new Promise((resolve) => child.once("exit", resolve));
    throw error;
  }
}

async function waitForServer(baseUrl) {
  const deadline = Date.now() + 15000;
  while (Date.now() < deadline) {
    try {
      const response = await fetch(`${baseUrl}/api/privacy-boundary`);
      if (response.ok) return;
    } catch {
      // Retry until the server is ready.
    }
    await new Promise((resolve) => setTimeout(resolve, 250));
  }
  fail(`server did not become ready: ${baseUrl}`);
}

async function uploadSample(baseUrl, sample) {
  const filePath = path.join(root, sample.file_path);
  const bytes = await readFile(filePath);
  const form = new FormData();
  form.append("video", new Blob([bytes], { type: "video/mp4" }), path.basename(sample.file_path));
  const response = await fetch(`${baseUrl}/api/upload-video`, {
    method: "POST",
    body: form
  });
  if (!response.ok) fail(`upload failed: ${response.status} ${await response.text()}`);
  return response.json();
}

async function postJson(url, body) {
  const response = await fetch(url, {
    method: "POST",
    headers: { "content-type": "application/json" },
    body: JSON.stringify(body)
  });
  if (!response.ok) fail(`request failed: ${url} ${response.status} ${await response.text()}`);
  return response.json();
}

async function fetchJson(url) {
  const response = await fetch(url);
  if (!response.ok) fail(`request failed: ${url} ${response.status} ${await response.text()}`);
  return response.json();
}

async function freePort() {
  const server = createServer();
  await new Promise((resolve) => server.listen(0, "127.0.0.1", resolve));
  const port = server.address().port;
  await new Promise((resolve) => server.close(resolve));
  return port;
}

function readArg(name) {
  const index = process.argv.indexOf(name);
  return index === -1 ? null : process.argv[index + 1] || null;
}

function assert(condition, message) {
  if (!condition) fail(message);
}

function fail(message) {
  throw new Error(`phase1 sample smoke failed: ${message}`);
}
